def utcnow() -> str:
    return datetime.now(UTC).isoformat()


class Database:
    def __init__(self, connection: Any, backend: str) -> None:
        self.connection = connection
        self.backend = backend

    def execute(self, query: str, params: tuple[Any, ...] = ()) -> Any:
        if self.backend == "postgres":
            query = query.replace("?", "%s")
        return self.connection.execute(query, params)

    def commit(self) -> None:
        self.connection.commit()

    def close(self) -> None:
        self.connection.close()


def is_postgres() -> bool:
    return bool(DATABASE_URL)


def is_production() -> bool:
    return os.environ.get("APP_ENV") == "production"


def string_agg_expr(value: str) -> str:
    if is_postgres():
        return f"STRING_AGG({value}, ',')"
    return f"GROUP_CONCAT({value})"


def connect() -> Database:
    if DATABASE_URL:
        import psycopg
        from psycopg.rows import dict_row

        return Database(psycopg.connect(DATABASE_URL, row_factory=dict_row), "postgres")

    DB_PATH.parent.mkdir(parents=True, exist_ok=True)
    conn = sqlite3.connect(DB_PATH)
    conn.row_factory = sqlite3.Row
    conn.execute("PRAGMA foreign_keys = ON")
    return Database(conn, "sqlite")


@contextmanager
def get_db() -> Iterator[Database]:
    conn = connect()
    try:
        yield conn
        conn.commit()
    finally:
        conn.close()


def _table_columns(db: Database, table_name: str) -> set[str]:
    if db.backend == "postgres":
        rows = db.execute(
            """
            SELECT column_name
            FROM information_schema.columns
            WHERE table_schema = current_schema() AND table_name = ?
            """,
            (table_name,),
        ).fetchall()
        return {row["column_name"] for row in rows}

    rows = db.execute(f"PRAGMA table_info({table_name})").fetchall()
    return {row["name"] for row in rows}


def ensure_shared_user_schema() -> None:
    """Keep the shared users table compatible with the vocabulary trainer."""
    migrations = [
        ("username", "ALTER TABLE users ADD COLUMN username TEXT"),
        ("email", "ALTER TABLE users ADD COLUMN email TEXT"),
        ("status", "ALTER TABLE users ADD COLUMN status TEXT NOT NULL DEFAULT 'approved'"),
        ("reviewed_by", "ALTER TABLE users ADD COLUMN reviewed_by INTEGER"),
        ("reviewed_at", "ALTER TABLE users ADD COLUMN reviewed_at TEXT"),
        ("rejection_reason", "ALTER TABLE users ADD COLUMN rejection_reason TEXT"),
        ("last_login_at", "ALTER TABLE users ADD COLUMN last_login_at TEXT"),
    ]

    with get_db() as db:
        if db.backend == "postgres":
            db.execute("SELECT pg_advisory_xact_lock(4030404)")
        columns = _table_columns(db, "users")
        for column_name, statement in migrations:
            if column_name not in columns:
                db.execute(statement)
                columns.add(column_name)

        rows = db.execute(
            "SELECT id, username, email FROM users WHERE email IS NULL OR email = ''"
        ).fetchall()
        for row in rows:
            username = (row["username"] or "").strip().lower()
            if username:
                db.execute(
                    "UPDATE users SET email = ? WHERE id = ?",
                    (f"{username}@legacy.local", row["id"]),
                )

        rows = db.execute(
            "SELECT id, username, email FROM users WHERE username IS NULL OR username = ''"
        ).fetchall()
        for row in rows:
            email = (row["email"] or "").strip().lower()
            if email:
                db.execute("UPDATE users SET username = ? WHERE id = ?", (email, row["id"]))

        db.execute("CREATE UNIQUE INDEX IF NOT EXISTS idx_users_email_shared ON users(email)")
        db.execute("CREATE UNIQUE INDEX IF NOT EXISTS idx_users_username_shared ON users(username)")
        db.execute("CREATE INDEX IF NOT EXISTS idx_users_status ON users(status)")

        if db.backend == "postgres":
            db.execute("ALTER TABLE users DROP CONSTRAINT IF EXISTS users_role_check")
            db.execute(
                "ALTER TABLE users ADD CONSTRAINT users_role_check "
                "CHECK (role IN ('admin', 'learner', 'student'))"
            )


