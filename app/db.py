def seed_users() -> None:
    configs = seed_user_configs()
    with get_db() as db:
        for config in configs:
            if is_production() and config["password_key"] not in os.environ:
                continue
            exists = db.execute(
                "SELECT * FROM users WHERE username = ? OR email = ? LIMIT 1",
                (config["username"], config["email"]),
            ).fetchone()
            if exists:
                password_changed = config["password_key"] in os.environ and not verify_password(
                    config["password"], exists["password_hash"]
                )
                role = (
                    config["role"]
                    if config["role"] == "admin"
                    else exists["role"] if exists["role"] in {"student", "learner"} else config["role"]
                )
                username = exists["username"] or config["username"]
                email = (
                    config["email"]
                    if not exists["email"] or exists["email"].endswith("@legacy.local")
                    else exists["email"]
                )
                profile_changed = (
                    exists["role"] != role
                    or exists["display_name"] != config["display_name"]
                    or exists["username"] != username
                    or exists["email"] != email
                )
                if password_changed or profile_changed:
                    db.execute(
                        """
                        UPDATE users
                        SET username = ?, email = ?, password_hash = ?, role = ?, display_name = ?
                        WHERE id = ?
                        """,
                        (
                            username,
                            email,
                            hash_password(config["password"]) if password_changed else exists["password_hash"],
                            role,
                            config["display_name"],
                            exists["id"],
                        ),
                    )
                continue
            db.execute(
                """
                INSERT INTO users (username, email, password_hash, role, status, display_name, created_at)
                VALUES (?, ?, ?, ?, 'approved', ?, ?)
                """,
                (
                    config["username"],
                    config["email"],
                    hash_password(config["password"]),
                    role_for_insert(db, config["role"]),
                    config["display_name"],
                    utcnow(),
                ),
            )


