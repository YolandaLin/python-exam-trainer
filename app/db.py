def shared_email(env_key: str, username: str) -> str:
    configured = os.environ.get(env_key, "").strip().lower()
    if configured:
        return configured
    if "@" in username:
        return username.strip().lower()
    return f"{username.strip().lower()}@legacy.local"


def role_for_insert(db: Database, desired_role: str) -> str:
    if desired_role != "student" or db.backend != "sqlite":
        return desired_role
    table = db.execute(
        "SELECT sql FROM sqlite_master WHERE type = 'table' AND name = 'users'"
    ).fetchone()
    if table and "'student'" not in (table["sql"] or ""):
        return "learner"
    return desired_role


