def init_db() -> None:
    with get_db() as db:
        for statement in schema_statements():
            db.execute(statement)

    seed_questions()
    seed_lessons()
    seed_users()


def concept_name(concept_id: str) -> str:
    return concept_id.replace("-", " ")


def unit_from_source(source_file: str) -> str:
    if source_file.startswith("01-"):
        return "第 1 講"
    if source_file.startswith("02-"):
        return "第 2 講"
    if source_file.startswith("03-"):
        return "第 3 講"
    if source_file.startswith("04-"):
        return "第 4 講"
    if source_file.startswith("05-"):
        return "第 5 講"
    if source_file.startswith("06-"):
        return "第 6 講"
    if source_file.startswith("07-"):
        return "第 7 講"
    if source_file.startswith("08-"):
        return "第 8 講"
    if source_file.startswith("09-"):
        return "第 9 講"
    if source_file.startswith("10-"):
        return "第 10 講"
    return ""


def load_questions() -> list[dict[str, Any]]:
    questions: list[dict[str, Any]] = []
    for path in sorted((ROOT / "content").glob("questions*.json")):
        questions.extend(json.loads(path.read_text(encoding="utf-8")))
    return questions


def load_misconception_question_ids() -> set[str]:
    mappings = json.loads(MISCONCEPTION_QUESTIONS_PATH.read_text(encoding="utf-8"))
    return {
        question_id
        for lesson_mappings in mappings.values()
        for question_ids in lesson_mappings
        for question_id in question_ids
    }


def seed_questions() -> None:
    questions = load_questions()
    with get_db() as db:
        for question in questions:
            db.execute(
                """
                INSERT INTO questions (
                    id, source_file, type, difficulty, stem, code, options_json,
                    answer_json, explanation, common_mistake, is_active
                )
                VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, 1)
                ON CONFLICT(id) DO UPDATE SET
                    source_file=excluded.source_file,
                    type=excluded.type,
                    difficulty=excluded.difficulty,
                    stem=excluded.stem,
                    code=excluded.code,
                    options_json=excluded.options_json,
                    answer_json=excluded.answer_json,
                    explanation=excluded.explanation,
                    common_mistake=excluded.common_mistake,
                    is_active=1
                """,
                (
                    question["id"],
                    question["source_file"],
                    question["type"],
                    question["difficulty"],
                    question["stem"],
                    question.get("code", ""),
                    json.dumps(question["options"], ensure_ascii=False),
                    json.dumps(question["answer"], ensure_ascii=False),
                    question["explanation"],
                    question["common_mistake"],
                ),
            )
            db.execute("DELETE FROM question_concepts WHERE question_id = ?", (question["id"],))
            for concept_id in question["concepts"]:
                db.execute(
                    """
                    INSERT INTO concepts (id, name, unit, description)
                    VALUES (?, ?, ?, '')
                    ON CONFLICT(id) DO UPDATE SET
                        name=excluded.name,
                        unit=CASE WHEN concepts.unit = '' THEN excluded.unit ELSE concepts.unit END
                    """,
                    (concept_id, concept_name(concept_id), unit_from_source(question["source_file"])),
                )
                db.execute(
                    "INSERT INTO question_concepts (question_id, concept_id, weight) VALUES (?, ?, 1.0)",
                    (question["id"], concept_id),
                )


