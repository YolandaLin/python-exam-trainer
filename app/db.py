ROOT = Path(__file__).resolve().parents[1]
DATABASE_URL = os.environ.get("DATABASE_URL")
DB_PATH = Path(os.environ.get("DB_PATH", ROOT / "data" / "app.db"))
LESSONS_PATH = ROOT / "content" / "lessons.json"
MISCONCEPTION_QUESTIONS_PATH = ROOT / "content" / "misconception_questions.json"


def utcnow() -> str:
    return datetime.now(UTC).isoformat()


class Database:
    def __init__(self, connection: Any, backend: str) -> None:
        self.connection = connection
        self.backend = backend

    def execute(self, query: str, params: tuple[Any, ...] = ()) -> Any:
        if self.backend == "postgres":
            query = query.replace("?", "%s")
        return self.connection.execute(query, params)

    def commit(self) -> None:
        self.connection.commit()

    def close(self) -> None:
        self.connection.close()


def is_postgres() -> bool:
    return bool(DATABASE_URL)


def is_production() -> bool:
    return os.environ.get("APP_ENV") == "production"


def string_agg_expr(value: str) -> str:
    if is_postgres():
        return f"STRING_AGG({value}, ',')"
    return f"GROUP_CONCAT({value})"


def connect() -> Database:
    if DATABASE_URL:
        import psycopg
        from psycopg.rows import dict_row

        return Database(psycopg.connect(DATABASE_URL, row_factory=dict_row), "postgres")

    DB_PATH.parent.mkdir(parents=True, exist_ok=True)
    conn = sqlite3.connect(DB_PATH)
    conn.row_factory = sqlite3.Row
    conn.execute("PRAGMA foreign_keys = ON")
    return Database(conn, "sqlite")


@contextmanager
def get_db() -> Iterator[Database]:
    conn = connect()
    try:
        yield conn
        conn.commit()
    finally:
        conn.close()


